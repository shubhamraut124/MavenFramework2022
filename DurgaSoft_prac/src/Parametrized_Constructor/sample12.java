package Parametrized_Constructor;

public class sample12 {

	public String name;			//declaring the variables with the public access modifiers
	public int age;
	
	
	sample12(String name1,int age1){	//creating the parameterized constructor same as class name 
		this.name=name1;				//passing the parameter value to the actual variables using " this " keyword
		this.age=age1;
		
	}
}
